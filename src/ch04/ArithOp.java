package ch04;

public class ArithOp {

	public static void main(String[] args) {
		int num1=7;
		int num2=3;
		double num3=7;
		double num4=3;
		
		System.out.println("num1+num2 = "+(num1+num2));
		System.out.println("num1-num2 = "+(num1-num2));
		System.out.println("num1*num2 = "+(num1*num2));
		System.out.println("num1/num2 = "+(num1/num2));
		System.out.println("num3%num4 = "+(num3%num4));
		

	}

}
